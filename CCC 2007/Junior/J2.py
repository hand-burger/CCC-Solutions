#Simple loop and check

while True:
    inputs = input('Enter phrase>')
    if(inputs == 'CU'):
        print('see you')
    elif(inputs == ':-)'):
        print("I'm happy")
    elif(inputs == ':-('):
        print("I'm unhappy")
    elif(inputs == ';-)'):
        print('wink')
    elif(inputs == ':-P'):
        print('stick out my tongue')
    elif(inputs == '( ̃. ̃)'):
        print('sleepy')
    elif(inputs == 'TA'):
        print('totally awesome')
    elif(inputs == 'CCC'):
        print('Canadian Computing Competition')
    elif(inputs == 'CUZ'):
        print('because')
    elif(inputs == 'TY'):
        print('thank you')
    elif(inputs == 'YW'):
        print("you're welcome")
    elif(inputs == 'TTYL'):
        print('talk to you later')
        break
    else:
        print(inputs)